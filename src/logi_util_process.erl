%% @doc プロセス関連のユーティリティ関数を集めたモジュール
-module(logi_util_process).

%%------------------------------------------------------------------------------------------------------------------------
%% Exported API
%%------------------------------------------------------------------------------------------------------------------------
-export([
         link_if_pid/1
        ]).

%%------------------------------------------------------------------------------------------------------------------------
%% Exported Functions
%%------------------------------------------------------------------------------------------------------------------------
%% @doc 引数がプロセスであるならlinkを張る
-spec link_if_pid(pid() | term()) -> ok.
link_if_pid(Pid) when is_pid(Pid) ->
    true = link(Pid),
    ok;
link_if_pid(_) -> ok.
